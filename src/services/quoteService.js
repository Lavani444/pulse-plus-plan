// Get a random quote from a specific category
export const getRandomQuote = (category = 'deep') => {
  const quotes = quoteCollection[category] || quoteCollection.deep;
  const randomIndex = Math.floor(Math.random() * quotes.length);
  return quotes[randomIndex];
};

// Get multiple random quotes
export const getMultipleQuotes = (count = 3, category = 'deep') => {
  const quotes = quoteCollection[category] || quoteCollection.deep;
  const shuffled = [...quotes].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, Math.min(count, quotes.length));
};

// Get quote of the day (changes daily but consistent per user)
export const getQuoteOfTheDay = (date = new Date(), category = 'deep') => {
  const quotes = quoteCollection[category] || quoteCollection.deep;
  const dayOfYear = Math.floor((date - new Date(date.getFullYear(), 0, 0)) / (1000 * 60 * 60 * 24));
  const index = dayOfYear % quotes.length;
  return quotes[index];
};